package Clases;

public class PareoProveedor {
    
    String nombreNuestro;
    String nombreChilemat;
    String rut;

    public String getNombreNuestro() {
        return nombreNuestro;
    }

    public void setNombreNuestro(String nombreNuestro) {
        this.nombreNuestro = nombreNuestro;
    }

    public String getNombreChilemat() {
        return nombreChilemat;
    }

    public void setNombreChilemat(String nombreChilemat) {
        this.nombreChilemat = nombreChilemat;
    }

    public String getRut() {
        return rut;
    }

    public void setRut(String rut) {
        this.rut = rut;
    }
}
