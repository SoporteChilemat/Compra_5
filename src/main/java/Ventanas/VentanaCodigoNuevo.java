    public static void crearTxt(String text1, String text2) {
        try {
            String s = System.lineSeparator() + text1 + "@" + text2;
            String cwd = System.getProperty("user.name");
            File file = new File("C:\\Users\\" + cwd + "\\Sync\\pareo.txt");
            Path p = Paths.get("C:\\Users\\" + cwd + "\\Sync\\pareo.txt");

            if (!file.exists()) {
                file.createNewFile();
            }

            try {
                String data = leerTxt();
                String[] split = data.split(System.getProperty("line.separator"));
                BufferedWriter writer = new BufferedWriter(new FileWriter(file));
                for (int i = 0; i < split.length; i++) {
                    String name = split[i];
                    System.out.println("name " + name);
                    String[] split1 = name.split("@");
                    String name1 = split1[0];
                    System.out.println("name1 " + name1);
                    if (name1.equals(text1)) {
                        writer.flush();
                        writer.close();
                        break;
                    }
                }
            } catch (Exception ex) {

            }
            Files.write(p, s.getBytes(), StandardOpenOption.APPEND);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

